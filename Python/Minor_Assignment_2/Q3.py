marks=float(input("Enter:"))
if marks>=90:
    print("Grade:A\nRemarks:Excellent")
elif marks>=80:
    print("Grade:B\nRemarks:Good")
elif marks>=70:
    print("Grade:C\nRemarks:Average")
elif marks>=60:
    print("Grade:D\nRemarks:Needs Improvement")
else:
    print("Grade:F\nRemarks:Failing")

